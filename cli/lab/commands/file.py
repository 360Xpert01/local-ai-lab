@file_cmd.command()
@click.argument('source')
@click.argument('destination')
def move(source, destination):
    """Move or rename a file."""
    src = Path(source)
    dst = Path(destination)
    
    if not src.exists():
        console.print(f"[red]Source not found: {source}[/red]")
        return
    
    dst.parent.mkdir(parents=True, exist_ok=True)
    src.rename(dst)
    console.print(f"[green]Moved:[/green] {source} → {destination}")


@file_cmd.command()
@click.argument('path')
@click.confirmation_option(prompt='Are you sure you want to delete this file?')
def delete(path):
    """Delete a file."""
    target = Path(path)
    
    if not target.exists():
        console.print(f"[red]File not found: {path}[/red]")
        return
    
    if target.is_dir():
        import shutil
        shutil.rmtree(target)
        console.print(f"[green]Deleted directory:[/green] {path}")
    else:
        target.unlink()
        console.print(f"[green]Deleted file:[/green] {path}")
