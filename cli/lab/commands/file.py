@file_cmd.command(name='list')
@click.argument('path', default='.')
@click.option('--recursive', '-r', is_flag=True, help='List recursively')
def list_files(path, recursive):
    """List files in a directory."""
    target = Path(path)
    
    if not target.exists():
        console.print(f"[red]Path not found: {path}[/red]")
        return
    
    if target.is_file():
        console.print(f"[red]{path} is a file, not a directory[/red]")
        return
    
    pattern = "**/*" if recursive else "*"
    files = list(target.glob(pattern))
    files = [f for f in files if not any(p.startswith('.') for p in f.parts)]
    
    for f in sorted(files):
        rel_path = f.relative_to(target)
        if f.is_dir():
            console.print(f"[blue]📁 {rel_path}/[/blue]")
        else:
            size = f.stat().st_size
            size_str = f"{size:,} B" if size < 1024 else f"{size/1024:.1f} KB"
            console.print(f"  📄 {rel_path} [dim]({size_str})[/dim]")


@file_cmd.command()
@click.argument('path')
def read(path):
    """Read and display a file with syntax highlighting."""
    target = Path(path)
    
    if not target.exists():
        console.print(f"[red]File not found: {path}[/red]")
        return
    
    if target.is_dir():
        console.print(f"[red]{path} is a directory[/red]")
        return
    
    try:
        content = target.read_text()
        
        # Detect language from extension
        ext = target.suffix.lstrip('.')
        lang_map = {
            'py': 'python',
            'js': 'javascript',
            'ts': 'typescript',
            'jsx': 'jsx',
            'tsx': 'tsx',
            'rs': 'rust',
            'go': 'go',
            'yaml': 'yaml',
            'yml': 'yaml',
            'json': 'json',
            'md': 'markdown',
            'html': 'html',
            'css': 'css',
            'sh': 'bash',
            'sql': 'sql',
        }
        
        syntax = Syntax(
            content,
            lang_map.get(ext, 'text'),
            theme='monokai',
            line_numbers=True,
            word_wrap=True
        )
        
        console.print(Panel(syntax, title=str(target)))
        
    except Exception as e:
        console.print(f"[red]Error reading file: {e}[/red]")


